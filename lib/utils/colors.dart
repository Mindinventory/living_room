import 'dart:ui';

import 'package:flutter/material.dart';

abstract class GradientColors {
  static const backgroundGradient = [
    Color(0xff00A99D),
    Color(0xff0B7B9D),
    Color(0xff1E2B9E),
  ];
}

abstract class AppColors {
  static const black = Colors.black;
  static const white = Colors.white;
  static const transparent = Colors.transparent;
  static const accentColor = Color(0xFFFF6600);
  static const lightBlackColor = Color(0xFF303030);
  static const redColor = Color(0xFFFF521C);
  static const yellowColor = Color(0xFFFFBF1C);
  static const cyanColor = Color(0xFF1CF1FF);
  static const blueColor = Color(0xFF1C51FF);
  static const purpleColor = Color(0xFF940C7E);
}
